    public
    static function isAndroidApp()
    {
        if (isset($_COOKIE['DEVICE']) && $_COOKIE['DEVICE'] == 'Android') {
            return true;
        }

        if (isset($_COOKIE['PRODUCT']) && $_COOKIE['PRODUCT'] == 'RocketWeb') {
            return true;
        }

        if (strpos($_SERVER['HTTP_REFERER'] ?? '', 'app=1')) {
            return true;
        }

        return false;
    }

